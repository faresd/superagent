function Fetcher() {
  this.params = {};
}

Fetcher.prototype.setRequestHeader = function(field, value) {
  if(!this.params.headers ) this.params.headers = {};
  this.params.headers[field] = value;
}

Fetcher.prototype.open = function(method, url){
  this.params.method = method;
  this.url = url;
}

Fetcher.prototype.send = function(payload){
  var urlFetchApp = UrlFetchApp;
  this.params['contentType'] = "application/json";
  this.params.muteHttpExceptions = true;
  if (payload) {
    this.params['payload'] = JSON.stringify(payload);
  }
  var result = urlFetchApp.fetch(this.url, this.params)
  return result;
}

/**
 * Removes leading and trailing whitespace, added to support IE.
 *
 * @param {String} s
 * @return {String}
 * @api private
 */

var trim = ''.trim
  ? function(s) { return s.trim(); }
  : function(s) { return s.replace(/(^\s*|\s*$)/g, ''); };

/**
 * Serialize the given `obj`.
 *
 * @param {Object} obj
 * @return {String}
 * @api private
 */

function serialize(obj) {
  if (!isObject(obj)) return obj;
  var pairs = [];
  for (var key in obj) {
    if (null != obj[key]) {
      pushEncodedKeyValuePair(pairs, key, obj[key]);
    }
  }
  return pairs.join('&');
}

/**
 * Helps 'serialize' with serializing arrays.
 * Mutates the pairs array.
 *
 * @param {Array} pairs
 * @param {String} key
 * @param {Mixed} val
 */

function pushEncodedKeyValuePair(pairs, key, val) {
  if (Array.isArray(val)) {
    return val.forEach(function(v) {
      pushEncodedKeyValuePair(pairs, key, v);
    });
  }
  pairs.push(encodeURIComponent(key)
    + '=' + encodeURIComponent(val));
}

